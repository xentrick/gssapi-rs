/*
 * This is an internal routine which validates the krb5_timestamps
 * field in a krb5_ticket.
 */
#[no_mangle]
#[c2rust::src_loc = "35:1"]
pub unsafe extern "C" fn krb5int_validate_times(mut context: krb5_context,
                                                mut times:
                                                    *mut krb5_ticket_times)
 -> krb5_error_code {
    let mut currenttime: krb5_timestamp = 0;
    let mut starttime: krb5_timestamp = 0;
    let mut retval: krb5_error_code = 0;
    retval = krb5_timeofday(context, &mut currenttime);
    if retval != 0 { return retval }
    /* if starttime is not in ticket, then treat it as authtime */
    if (*times).starttime != 0 as libc::c_int {
        starttime = (*times).starttime
    } else { starttime = (*times).authtime } /* ticket not yet valid */
    if ts_after(starttime, ts_incr(currenttime, (*context).clockskew)) != 0 {
        return -(1765328351 as libc::c_long) as krb5_error_code
    } /* ticket expired */
    if ts_after(currenttime, ts_incr((*times).endtime, (*context).clockskew))
           != 0 {
        return -(1765328352 as libc::c_long) as krb5_error_code
    }
    return 0 as libc::c_int;
}
