/* sockaddr2krbaddr() utility function used by kprop and kpropd */
/*
 * Convert an IPv4 or IPv6 socket address to a newly allocated krb5_address.
 * There is similar code elsewhere in the tree, so this should possibly become
 * a libkrb5 API in the future.
 */
#[no_mangle]
#[c2rust::src_loc = "40:1"]
pub unsafe extern "C" fn sockaddr2krbaddr(mut context: krb5_context,
                                          mut family: libc::c_int,
                                          mut sa: *mut sockaddr,
                                          mut dest: *mut *mut krb5_address)
 -> libc::c_int {
    let mut addr: krb5_address =
        krb5_address{magic: 0,
                     addrtype: 0,
                     length: 0,
                     contents: 0 as *mut krb5_octet,};
    addr.magic = -(1760647390 as libc::c_long) as krb5_magic;
    if family == 2 as libc::c_int {
        let mut sa4: *mut sockaddr_in = sa2sin(sa);
        addr.addrtype = 0x2 as libc::c_int;
        addr.length =
            ::std::mem::size_of::<in_addr>() as libc::c_ulong as libc::c_uint;
        addr.contents =
            &mut (*sa4).sin_addr as *mut in_addr as *mut krb5_octet
    } else if family == 10 as libc::c_int {
        let mut sa6: *mut sockaddr_in6 = sa2sin6(sa);
        if ({
                let mut __a: *const in6_addr =
                    &mut (*sa6).sin6_addr as *mut in6_addr as *const in6_addr;
                ((*__a).__in6_u.__u6_addr32[0 as libc::c_int as usize] ==
                     0 as libc::c_int as libc::c_uint &&
                     (*__a).__in6_u.__u6_addr32[1 as libc::c_int as usize] ==
                         0 as libc::c_int as libc::c_uint &&
                     (*__a).__in6_u.__u6_addr32[2 as libc::c_int as usize] ==
                         htonl(0xffff as libc::c_int as uint32_t)) as
                    libc::c_int
            }) != 0 {
            addr.addrtype = 0x2 as libc::c_int;
            addr.contents =
                (&mut (*sa6).sin6_addr as *mut in6_addr as
                     *mut krb5_octet).offset(12 as libc::c_int as isize);
            addr.length = 4 as libc::c_int as libc::c_uint
        } else {
            addr.addrtype = 0x18 as libc::c_int;
            addr.length =
                ::std::mem::size_of::<in6_addr>() as libc::c_ulong as
                    libc::c_uint;
            addr.contents =
                &mut (*sa6).sin6_addr as *mut in6_addr as *mut krb5_octet
        }
    } else { return -(1765328170 as libc::c_long) as libc::c_int }
    return krb5_copy_addr(context, &mut addr, dest);
}
/* Construct a host-based principal, similar to krb5_sname_to_principal() but
 * with a specified realm. */
#[no_mangle]
#[c2rust::src_loc = "71:1"]
pub unsafe extern "C" fn sn2princ_realm(mut context: krb5_context,
                                        mut hostname: *const libc::c_char,
                                        mut sname: *const libc::c_char,
                                        mut realm: *const libc::c_char,
                                        mut princ_out: *mut krb5_principal)
 -> krb5_error_code {
    let mut ret: krb5_error_code = 0;
    let mut canonhost: *mut libc::c_char = 0 as *mut libc::c_char;
    let mut localname: [libc::c_char; 64] = [0; 64];
    *princ_out = 0 as krb5_principal;
    if !sname.is_null() && !realm.is_null() {
    } else {
        __assert_fail(b"sname != NULL && realm != NULL\x00" as *const u8 as
                          *const libc::c_char,
                      b"kprop_util.c\x00" as *const u8 as *const libc::c_char,
                      79 as libc::c_int as libc::c_uint,
                      (*::std::mem::transmute::<&[u8; 105],
                                                &[libc::c_char; 105]>(b"krb5_error_code sn2princ_realm(krb5_context, const char *, const char *, const char *, krb5_principal *)\x00")).as_ptr());
    }
    /* If hostname is NULL, use the local hostname. */
    if hostname.is_null() {
        if gethostname(localname.as_mut_ptr(), 64 as libc::c_int as size_t) !=
               0 as libc::c_int {
            return *__errno_location()
        }
        hostname = localname.as_mut_ptr()
    }
    ret = krb5_expand_hostname(context, hostname, &mut canonhost);
    if ret != 0 { return ret }
    ret =
        krb5_build_principal(context, princ_out,
                             strlen(realm) as libc::c_uint, realm, sname,
                             canonhost,
                             0 as *mut libc::c_void as *mut libc::c_char);
    krb5_free_string(context, canonhost);
    if ret == 0 { (**princ_out).type_0 = 3 as libc::c_int }
    return ret;
}
