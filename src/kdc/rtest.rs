unsafe extern "C" fn make_princ(mut ctx: krb5_context,
                                mut str: *const libc::c_char,
                                mut prog: *const libc::c_char)
 -> krb5_principal {
    let mut ret: krb5_principal = 0 as *mut krb5_principal_data;
    let mut dat: *mut libc::c_char = 0 as *mut libc::c_char;
    ret =
        malloc(::std::mem::size_of::<krb5_principal_data>() as libc::c_ulong)
            as krb5_principal;
    if ret.is_null() {
        com_err(prog, 12 as libc::c_int as errcode_t,
                b"while allocating principal data\x00" as *const u8 as
                    *const libc::c_char);
        exit(3 as libc::c_int);
    }
    memset(ret as *mut libc::c_void, 0 as libc::c_int,
           ::std::mem::size_of::<krb5_principal_data>() as libc::c_ulong);
    /* We do not include the null... */
    dat = malloc(strlen(str)) as *mut libc::c_char;
    if dat.is_null() {
        com_err(prog, 12 as libc::c_int as errcode_t,
                b"while allocating principal realm data\x00" as *const u8 as
                    *const libc::c_char);
        exit(3 as libc::c_int);
    }
    memcpy(dat as *mut libc::c_void, str as *const libc::c_void, strlen(str));
    (*ret).realm.data = dat;
    (*ret).realm.length = strlen(str) as libc::c_uint;
    return ret;
}
