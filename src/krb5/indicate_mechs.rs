#[no_mangle]

pub unsafe extern "C" fn krb5_gss_indicate_mechs(
    mut minor_status: *mut crate::gssapi_h::OM_uint32,
    mut mech_set: *mut crate::gssapi_h::gss_OID_set,
) -> crate::gssapi_h::OM_uint32 {
    return crate::src::generic::oid_ops::generic_gss_copy_oid_set(
        minor_status,
        crate::src::krb5::gssapi_krb5::kg_all_mechs as *const crate::gssapi_h::gss_OID_set_desc,
        mech_set,
    );
}
