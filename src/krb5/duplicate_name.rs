pub unsafe extern "C" fn krb5_gss_duplicate_name(
    mut minor_status: *mut crate::gssapi_h::OM_uint32,
    input_name: crate::gssapi_h::gss_name_t,
    mut dest_name: *mut crate::gssapi_h::gss_name_t,
) -> crate::gssapi_h::OM_uint32 {
    let mut context = 0 as *mut crate::k5_int_h::_krb5_context;
    let mut code: crate::krb5_h::krb5_error_code = 0;
    let mut princ = 0 as *mut crate::gssapiP_krb5_h::_krb5_gss_name_rec;
    let mut outprinc = 0 as *mut crate::gssapiP_krb5_h::_krb5_gss_name_rec;
    *minor_status = 0u32;
    code = crate::src::krb5::init_sec_context::krb5_gss_init_context(&mut context);
    if code != 0 {
        *minor_status = code as crate::gssapi_h::OM_uint32;
        return (13u32) << 16i32;
    }
    princ = input_name as crate::gssapiP_krb5_h::krb5_gss_name_t;
    code = crate::src::krb5::naming_exts::kg_duplicate_name(context, princ, &mut outprinc);
    if code != 0 {
        *minor_status = code as crate::gssapi_h::OM_uint32;
        crate::src::krb5::disp_status::krb5_gss_save_error_info(*minor_status, context);
        crate::krb5_h::krb5_free_context(context);
        return (13u32) << 16i32;
    }
    crate::krb5_h::krb5_free_context(context);
    *dest_name = outprinc as crate::gssapi_h::gss_name_t;
    return 0u32;
}
