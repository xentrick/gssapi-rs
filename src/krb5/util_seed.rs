#[no_mangle]

pub unsafe extern "C" fn kg_make_seed(
    mut context: crate::krb5_h::krb5_context,
    mut key: crate::krb5_h::krb5_key,
    mut seed: *mut u8,
) -> crate::krb5_h::krb5_error_code {
    let mut code: crate::krb5_h::krb5_error_code = 0;
    let mut rkey = 0 as crate::krb5_h::krb5_key;
    let mut tmpkey = 0 as *mut crate::krb5_h::krb5_keyblock;
    let mut kb = 0 as *mut crate::krb5_h::krb5_keyblock;
    let mut i: u32 = 0;
    code = crate::krb5_h::krb5_k_key_keyblock(context, key, &mut tmpkey);
    if code != 0 {
        return code;
    }
    /* reverse the key bytes, as per spec */
    kb = &mut (*key).keyblock;
    i = 0u32;
    while i < (*tmpkey).length {
        *(*tmpkey).contents.offset(i as isize) = *(*kb)
            .contents
            .offset((*kb).length.wrapping_sub(1u32).wrapping_sub(i) as isize);
        i = i.wrapping_add(1)
    }
    code = crate::krb5_h::krb5_k_create_key(context, tmpkey, &mut rkey);
    if !(code != 0) {
        code = crate::src::krb5::util_crypt::kg_encrypt(
            context,
            rkey,
            22i32,
            0 as *mut libc::c_void,
            zeros.as_ptr() as crate::krb5_h::krb5_const_pointer,
            seed as crate::krb5_h::krb5_pointer,
            16u32,
        )
    }
    crate::krb5_h::krb5_free_keyblock(context, tmpkey);
    crate::krb5_h::krb5_k_free_key(context, rkey);
    return code;
}
