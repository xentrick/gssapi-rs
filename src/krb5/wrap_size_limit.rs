#[no_mangle]

pub unsafe extern "C" fn krb5_gss_wrap_size_limit(
    mut minor_status: *mut crate::gssapi_h::OM_uint32,
    mut context_handle: crate::gssapi_h::gss_ctx_id_t,
    mut conf_req_flag: i32,
    mut qop_req: crate::gssapi_h::gss_qop_t,
    mut req_output_size: crate::gssapi_h::OM_uint32,
    mut max_input_size: *mut crate::gssapi_h::OM_uint32,
) -> crate::gssapi_h::OM_uint32 {
    let mut ctx = 0 as *mut crate::gssapiP_krb5_h::krb5_gss_ctx_id_rec;
    let mut data_size: crate::gssapi_h::OM_uint32 = 0;
    let mut conflen: crate::gssapi_h::OM_uint32 = 0;
    let mut ohlen: crate::gssapi_h::OM_uint32 = 0;
    let mut overhead: i32 = 0;
    /* only default qop is allowed */
    if qop_req != 0u32 {
        *minor_status = -(2045022968 as isize) as crate::gssapi_h::OM_uint32;
        return (14u32) << 16i32;
    }
    ctx = context_handle as *mut crate::gssapiP_krb5_h::krb5_gss_ctx_id_rec;
    if (*ctx).terminated() as i32 != 0 || (*ctx).established() == 0 {
        *minor_status = 39756039u32;
        return (8u32) << 16i32;
    }
    if (*ctx).proto == 1i32 {
        /* No pseudo-ASN.1 wrapper overhead, so no sequence length and
        OID.  */
        let mut sz = req_output_size;
        /* Token header: 16 octets.  */
        if conf_req_flag != 0 {
            let mut key = 0 as *mut crate::k5_int_h::krb5_key_st;
            let mut enctype: crate::krb5_h::krb5_enctype = 0;
            key = if (*ctx).have_acceptor_subkey() as i32 != 0 {
                (*ctx).acceptor_subkey
            } else {
                (*ctx).subkey
            };
            enctype = (*key).keyblock.enctype;
            while sz > 0u32
                && crate::krb5_h::krb5_encrypt_size(sz as crate::stddef_h::size_t, enctype)
                    .wrapping_add(16usize)
                    > req_output_size as usize
            {
                sz = sz.wrapping_sub(1)
            }
            /* Allow for encrypted copy of header.  */
            if sz > 16u32 {
                sz = (sz).wrapping_sub(16u32)
            } else {
                sz = 0u32
            }
        } else {
            let mut cksumtype: crate::krb5_h::krb5_cksumtype = 0;
            let mut err: crate::krb5_h::krb5_error_code = 0;
            let mut cksumsize: crate::stddef_h::size_t = 0;
            cksumtype = if (*ctx).have_acceptor_subkey() as i32 != 0 {
                (*ctx).acceptor_subkey_cksumtype
            } else {
                (*ctx).cksumtype
            };
            err =
                crate::krb5_h::krb5_c_checksum_length((*ctx).k5_context, cksumtype, &mut cksumsize);
            if err != 0 {
                *minor_status = err as crate::gssapi_h::OM_uint32;
                return (13u32) << 16i32;
            }
            /* Allow for token header and checksum.  */
            if (sz as usize) < (16usize).wrapping_add(cksumsize) {
                sz = 0u32
            } else {
                sz = (sz as usize).wrapping_sub((16usize).wrapping_add(cksumsize))
                    as crate::gssapi_h::OM_uint32
            }
        }
        *max_input_size = sz;
        *minor_status = 0u32;
        return 0u32;
    }
    /* Calculate the token size and subtract that from the output size */
    overhead = (7u32).wrapping_add((*(*ctx).mech_used).length) as i32;
    data_size = req_output_size;
    conflen = crate::src::krb5::util_crypt::kg_confounder_size(
        (*ctx).k5_context,
        (*(*ctx).enc).keyblock.enctype,
    ) as crate::gssapi_h::OM_uint32;
    data_size = conflen.wrapping_add(data_size).wrapping_add(8u32) & !(7u32);
    ohlen = crate::src::generic::util_token::gssint_g_token_size(
        (*ctx).mech_used,
        (data_size as usize)
            .wrapping_add((*ctx).cksum_size)
            .wrapping_add(14usize) as u32,
    )
    .wrapping_sub(req_output_size);
    if ohlen.wrapping_add(overhead as u32) < req_output_size {
        /*
         * Cannot have trailer length that will cause us to pad over our
         * length.
         */
        *max_input_size = req_output_size
            .wrapping_sub(ohlen)
            .wrapping_sub(overhead as u32)
            & !(7u32)
    } else {
        *max_input_size = 0u32
    }
    *minor_status = 0u32;
    return 0u32;
}
